/* Macro Settings */
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    // Toggle IME input mode (Hiragana <-> Halfwidth Eisu)
    case KANA:
      if (record->event.pressed) {
        SEND_STRING(SS_LALT("`"));  // Alt + Enter
      }
      break;
  }
  return true;
};